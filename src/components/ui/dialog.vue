<script setup lang="ts">
import { DialogRoot } from 'radix-vue';

interface Props {
  open?: boolean;
  defaultOpen?: boolean;
  modal?: boolean;
}

defineProps<Props>();
defineEmits<{
  'update:open': [value: boolean];
}>();
</script>

<template>
  <DialogRoot
    :open="open"
    :default-open="defaultOpen"
    :modal="modal"
    @update:open="$emit('update:open', $event)"
  >
    <slot />
  </DialogRoot>
</template>
